class Package:
  def __init__(self, ID, address, deadline, city, zip_code, state, weight, status, departure_time, delivery_time):
    self.ID = ID
    self.address = address
    self.deadline = deadline
    self.city = city
    self.zip_code = zip_code
    self.state = state
    self.weight = weight
    self.status = status
    self.departure_time = departure_time
    self.delivery_time = delivery_time


